    private void cargarMatches() {        
        List<MatchDTO> matches = USUARIO_SERVICE.mostrarMatches(loggedUser.getIdUsuario());
        if(matches == null) {
            saltyMessageLabel.setVisible(true);
            return;
        }
        
        matches.forEach(p -> {
            panelLista.add(new MatchRegistroPanel(p));
        });
         panelLista.revalidate();
    }

